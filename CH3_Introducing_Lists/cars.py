#Ascending Sorting
cars = ["bmw", "audi", "toyota", "subaru"]
cars.sort()
print(cars)

#Reverse Sorting
cars.sort(reverse=True)
print(cars)

#Sorting Temporarily
print("\nHere is the original list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)

#Print a List in Reverse Order
cars = ["bmw", "audi", "toyota", "subaru"]
print("\n")
print(cars)
cars.reverse()
print(cars)

#Length of a List
cars = ["bmw", "audi", "toyota", "subaru"]
print(len(cars))
